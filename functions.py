# Example of a function that says hello to a name
def say_hello(name):
    print (f"Hello, {name}!")

say_hello("Alice")  # This will print "Hello, Alice!"
say_hello("Bob")    # This will print "Hello, Bob!"

# Example of a function receive a number and return its square
def square(number):
    return number ** 2
result = square(5)  # This will return 25
print(result)       # This will print 25

# Example of a function that takes two numbers and returns their sum
def sum(a, b):
    return a + b
sum_result = sum(3, 4)  # This will return 7
print(sum_result)        # This will print 7